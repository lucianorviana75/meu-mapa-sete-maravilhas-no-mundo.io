for i in range(len(cord_lugar)):
  coordenada = cord_lugar[i]
  maravilha = sete_maravilhas[i]
  folium.Marker(coordenada, popup=maravilha, icon= folium.Icon(icon="glyphicon glyphicon-camera",
                                                              color= "black",
                                                              icon_color="white",
                                                              prefix="glyphicon")).add_to(local_map)
local_map.add_child(folium.LatLngPopup())
local_map.save("Sete_Maravilhas.html")
